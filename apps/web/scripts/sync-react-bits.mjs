#!/usr/bin/env node
/**
 * Installs React Bits components from the catalog via shadcn CLI and
 * regenerates src/lib/react-bits/manifest.ts for dynamic imports.
 */
import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(__dirname, "..");
const catalogPath = path.resolve(webRoot, "../../packages/shared/react-bits.catalog.json");
const manifestPath = path.resolve(webRoot, "src/lib/react-bits/manifest.ts");
const componentsDir = path.resolve(webRoot, "src/components");

const REGISTRY_URL = "https://reactbits.dev/r/registry.json";

function readCatalog() {
  const raw = JSON.parse(fs.readFileSync(catalogPath, "utf8"));
  return raw.components ?? [];
}

async function fetchRegistry() {
  const res = await fetch(REGISTRY_URL);
  if (!res.ok) throw new Error(`Failed to fetch registry: ${res.status}`);
  return res.json();
}

function installComponent(slug) {
  console.log(`\n→ Installing @react-bits/${slug}...`);
  try {
    execSync(`npx shadcn@latest add "@react-bits/${slug}" --yes --overwrite`, {
      cwd: webRoot,
      stdio: "inherit",
      env: { ...process.env, CI: "true" },
    });
    return true;
  } catch (err) {
    console.warn(`  ⚠ Failed to install ${slug}:`, err.message ?? err);
    return false;
  }
}

function resolveImportPath(registryItem, slug) {
  const title = registryItem.title ?? slug.replace(/-(TS|JS)-(TW|CSS)$/, "");

  // shadcn CLI installs React Bits as flat files: src/components/{Title}.tsx
  const flatTsx = path.join(componentsDir, `${title}.tsx`);
  if (fs.existsSync(flatTsx)) {
    return `@/components/${title}`;
  }

  const mainFile = registryItem.files?.find((f) => f.path?.match(/\.(tsx|jsx)$/));
  if (!mainFile?.path) return null;
  const parts = mainFile.path.split("/");
  const folder = parts[0];
  const fileName = path.basename(mainFile.path, path.extname(mainFile.path));
  const absFolder = path.join(componentsDir, folder);
  if (!fs.existsSync(absFolder)) return null;

  const candidates = [
    path.join(absFolder, `${fileName}.tsx`),
    path.join(absFolder, `${fileName}.jsx`),
    path.join(absFolder, "index.tsx"),
    path.join(absFolder, "index.jsx"),
  ];
  for (const c of candidates) {
    if (fs.existsSync(c)) {
      const rel = path.relative(path.resolve(webRoot, "src"), c).replace(/\\/g, "/");
      return `@/${rel.replace(/\.(tsx|jsx)$/, "")}`;
    }
  }

  const files = fs.readdirSync(absFolder);
  const tsx = files.find((f) => f.endsWith(".tsx") || f.endsWith(".jsx"));
  if (tsx) {
    const rel = path.relative(path.resolve(webRoot, "src"), path.join(absFolder, tsx)).replace(/\\/g, "/");
    return `@/${rel.replace(/\.(tsx|jsx)$/, "")}`;
  }
  return null;
}

function generateManifest(entries) {
  const lines = [
    "/* eslint-disable */",
    "// AUTO-GENERATED by scripts/sync-react-bits.mjs — do not edit manually",
    "import type { ComponentType, ReactNode } from \"react\";",
    "",
    "export interface ReactBitsManifestEntry {",
    "  slug: string;",
    "  title: string;",
    "  description: string;",
    "  category: string;",
    "  supportsChildren: boolean;",
    "  dependencies: string[];",
    "  defaultProps: Record<string, unknown>;",
    "  load: () => Promise<{ default: ComponentType<Record<string, unknown>> }>;",
    "}",
    "",
    "export const REACT_BITS_MANIFEST: Record<string, ReactBitsManifestEntry> = {",
  ];

  for (const e of entries) {
    lines.push(`  ${JSON.stringify(e.slug)}: {`);
    lines.push(`    slug: ${JSON.stringify(e.slug)},`);
    lines.push(`    title: ${JSON.stringify(e.title)},`);
    lines.push(`    description: ${JSON.stringify(e.description)},`);
    lines.push(`    category: ${JSON.stringify(e.category)},`);
    lines.push(`    supportsChildren: ${e.supportsChildren},`);
    lines.push(`    dependencies: ${JSON.stringify(e.dependencies)},`);
    lines.push(`    defaultProps: ${JSON.stringify(e.defaultProps)},`);
    lines.push(`    load: () => import(${JSON.stringify(e.importPath)}) as unknown as Promise<{ default: ComponentType<Record<string, unknown>> }>,`);
    lines.push("  },");
  }

  lines.push("};");
  lines.push("");
  lines.push("export const REACT_BITS_SLUGS = Object.keys(REACT_BITS_MANIFEST);");
  lines.push("");
  lines.push("export function getReactBitsEntry(slug: string): ReactBitsManifestEntry | undefined {");
  lines.push("  return REACT_BITS_MANIFEST[slug];");
  lines.push("}");
  lines.push("");

  fs.mkdirSync(path.dirname(manifestPath), { recursive: true });
  fs.writeFileSync(manifestPath, lines.join("\n"));
  console.log(`\n✓ Wrote manifest with ${entries.length} entries → ${manifestPath}`);
}

async function main() {
  const catalog = readCatalog();
  const registry = await fetchRegistry();
  const registryByName = new Map(registry.items.map((i) => [i.name, i]));

  const skipInstall = process.argv.includes("--manifest-only");
  const installed = [];

  for (const item of catalog) {
    const slug = item.slug;
    const regItem = registryByName.get(slug);
    if (!regItem) {
      console.warn(`⚠ Slug not in registry: ${slug}`);
      continue;
    }

    if (!skipInstall) {
      installComponent(slug);
    }

    const importPath = resolveImportPath(regItem, slug);
    if (!importPath) {
      console.warn(`⚠ Could not resolve import path for ${slug}`);
      continue;
    }

    installed.push({
      slug,
      title: regItem.title ?? slug.replace(/-TS-TW$/, ""),
      description: regItem.description ?? "",
      category: item.category ?? "other",
      supportsChildren: Boolean(item.supportsChildren),
      dependencies: regItem.dependencies ?? [],
      defaultProps: inferDefaultProps(regItem),
      importPath,
    });
  }

  if (installed.length === 0) {
    console.error("No components installed. Aborting manifest generation.");
    process.exit(1);
  }

  generateManifest(installed);
  annotateInstalledFiles();
}

function annotateInstalledFiles() {
  const skip = new Set(["PublicPage.tsx", "PublicPageRenderer.tsx", "AdminShell.tsx"]);
  for (const file of fs.readdirSync(componentsDir)) {
    if (!file.endsWith(".tsx") && !file.endsWith(".ts")) continue;
    if (skip.has(file)) continue;
    if (file === "HyperSpeedPresets.ts") continue;
    const full = path.join(componentsDir, file);
    if (fs.statSync(full).isDirectory()) continue;
    // Only annotate flat shadcn-installed component files at src/components/*.tsx
    const rel = path.relative(path.join(webRoot, "src"), full);
    if (rel.includes(`${path.sep}builder${path.sep}`) || rel.includes(`${path.sep}react-bits${path.sep}`)) continue;
    let content = fs.readFileSync(full, "utf8");
    if (!content.startsWith("// @ts-nocheck")) {
      fs.writeFileSync(full, `// @ts-nocheck\n${content}`);
    }
  }
}

function inferDefaultProps(regItem) {
  const props = {};
  const desc = regItem.description ?? "";
  if (/fade|animate|scroll|wrapper/i.test(desc) || /Content|Reveal|Blur|Magnet|Glare|StarBorder|ElectricBorder|DecayCard/i.test(regItem.title ?? "")) {
    if (/FadeContent|AnimatedContent|ScrollReveal|ScrollFloat|GradualBlur/i.test(regItem.title ?? "")) {
      Object.assign(props, { duration: 1000, threshold: 0.1 });
    }
  }
  if (/text|Text|CountUp/i.test(regItem.title ?? "")) {
    Object.assign(props, { text: "Hello World" });
  }
  return props;
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
